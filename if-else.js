// -----Condicionais if, if-else-----
// -----if-----
// cria variaveis para testes
var a = 2;
var b = 1;
// estrutura = if(condiçao) {codigo a ser executado caso for verdade}
// -----Exemplos-----
// verifica se a é maior que b
if (a>b) {
	alert("a é maior que b"); // <--- se for verdade faz isso 
}
// -----if-else-----
// verifica se a é menor que b
if (a>b){
	alert("a é menor que b"); // <--- se for verdade faz isso
}
// o esle é executado caso o if retornar falso
else{
	alert("a nao é menor que b")
}
// podemos compreender o if e else como se fosse: se isso for verdade fassa isso... se nao fassa isso
